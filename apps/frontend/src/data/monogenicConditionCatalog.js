export function catalogMatchesForCondition(condition) {
  const normalized = normalize(condition);
  return MONOGENIC_CONDITION_CATALOG.filter((entry) => {
    if (normalize(entry.condition) === normalized) return true;
    if (normalized.includes(normalize(entry.condition)) || normalize(entry.condition).includes(normalized)) return true;
    return false;
  });
}

export function filterCatalog(query, category = "all") {
  const q = normalize(query);
  return MONOGENIC_CONDITION_CATALOG.filter((entry) => {
    if (category !== "all" && entry.category !== category) return false;
    if (!q) return true;
    const haystack = normalize([
      entry.condition,
      entry.category,
      ...(entry.genes || []),
      ...(entry.inheritance || []),
    ].filter(Boolean).join(" "));
    return haystack.includes(q);
  });
}

export function catalogCategories() {
  return ["all", ...Array.from(new Set(MONOGENIC_CONDITION_CATALOG.map((entry) => entry.category))).sort()];
}

function normalize(value) {
  return String(value || "").toLowerCase().replace(/[^a-z0-9ąćęłńóśźż]+/g, " " ).trim();
}
